//Classes: Constructor e This

//This == Isto. Se referente ao próprio objeto em si.

class Person {
    constructor(name, age){  //Constructor vai criar as caracteristicas/propriedades do objeto
        this.name = name; //This está dizendo que "Person.(NomeDaPessoa) = NomeDaPessoa"
        this.age = age; //This está dizendo que "Person.(Idade) = Idade"
    }
}