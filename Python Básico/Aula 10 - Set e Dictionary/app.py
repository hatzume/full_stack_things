# Coleções: Set e dictionary

# Exemplo de Set:
frutas = {"maçã","Laranja","Abacaxi"}

frutas.add("Pera") # Adicionar fruta na lista desordenada

frutas.remove("maçã") # Remover fruta da lista desordenada

frutas.pop()

print(frutas)
# Sempre que eu printar o set, ele vai mandar a lista desordenada
# Set pode receber string, int, bool, mas não recebe itens duplicados, não dá para adicionar duas maçãs

